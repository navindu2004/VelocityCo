            <h2>MAJOR THREATS</h2>
            
            <div class="container1">
                <img src="ImageS/bycatch.jpg" alt="Fisheries bycatch" class="imageT">
                <div class="overlay">
                  <div class="textT">Fisheries Bycatch</div>
                </div>
              </div>
              <p class="PT1">
              It’s estimated that the fishing industry contributes to the death of thousands to tens of thousands of sea turtles each year. Turtles that become trapped in longlines, gill nets and trawls are thrown away as bycatch. And those that manage to avoid fishing nets are impacted by the disruption to their food supply and habitat.</p>
              <br>
              <br>
              <div class="container2">
                <img src="ImageS/Coastal+development.jpg" alt="Coastal Development" class="imageT2">
                <div class="overlay">
                  <div class="textT2">Coastal Development</div>
                </div>
              </div>
              <p class="PT2">Every year, sea turtle habitats are destroyed because of shrinking coastlines. Wherever there is boat vessel traffic, whenever a new hotel or high-rise is built up along the shore, and wherever there is sea floor dredging and beach erosion sea turtle food supplies and nesting areas take a major hit.</p>
              <br>
              <br>
              <div class="container3">
                <img src="ImageS/river-pollution.jpg" alt="Pollution and pathogens" class="imageT3">
                <div class="overlay">
                  <div class="textT3">Pollution and pathogens</div>
                </div>
              </div>
              <p class="PT3">Marine pollution can harm sea turtles in many ways. Plastic pollution, discarded fishing gear, petroleum by-products, and other debris injure sea turtles through ingestion and entanglement. Ocean pollution can also weaken the turtles’ immune systems, and disrupt nesting behavior and hatchling orientation.</p>
